CREATE OR REPLACE FUNCTION get_tree_id(key text) RETURNS uuid AS $$
BEGIN
    RETURN CASE key
        WHEN 'hp'   THEN '00000001-0000-0000-0000-000000000000'::uuid
        WHEN 'got'  THEN '00000002-0000-0000-0000-000000000000'::uuid
        WHEN 'ete'  THEN '00000003-0000-0000-0000-000000000000'::uuid

        ELSE NULL
    END;
END;
$$ LANGUAGE plpgsql IMMUTABLE;
